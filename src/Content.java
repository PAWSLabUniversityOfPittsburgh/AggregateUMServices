

public class Content {
	private String idName;
	private String type;
	
	public Content(String idName, String type) {
		super();
		this.idName = idName;
		this.type = type;
	}
	public String getIdName() {
		return idName;
	}
	public void setIdName(String idName) {
		this.idName = idName;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	
	
}
